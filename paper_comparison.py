def _ascii_chart(values: list, label: str = "", width: int = 60):
    mn, mx = min(values), max(values)
    span = max(mx - mn, 1e-6)
    bar  = ""
    for v in values:
        pos = int((v - mn) / span * (width - 1))
        bar += "▓" if v >= 0 else "░"
    zero_pos = int((0 - mn) / span * (width - 1))
    axis = " " * zero_pos + "│"
    print(f"  {label}  [{mn:+.2f}] {bar} [{mx:+.2f}]")


def save_plots(r: dict):
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
        import numpy as np

        fig, axes = plt.subplots(1, 3, figsize=(16, 5))
        fig.suptitle("Adaptive Aging-Aware DNN Accelerator — Results vs. Paper", fontsize=13, fontweight="bold")

        # Plot 1: PPO reward curves
        ax = axes[0]
        iters = list(range(1, 41))
        paper_r = PAPER["ppo_rewards"]
        our_r   = r["ppo"]["rewards"]
        ax.plot(iters, paper_r, "b--o", markersize=3, label="Paper", alpha=0.7)
        ax.plot(iters, our_r,   "r-o",  markersize=3, label="Ours (improved)")
        ax.axhline(0, color="gray", linewidth=0.8, linestyle=":")
        ax.set_xlabel("Iteration"); ax.set_ylabel("Mean Reward")
        ax.set_title("PPO Reward Curve (40 Iterations)")
        ax.legend(); ax.grid(alpha=0.3)

        # Plot 2: NSGA-II per-workload aging reduction
        ax = axes[1]
        workloads = list(PAPER["nsga2"].keys())
        paper_red = [PAPER["nsga2"][w]["reduction"] for w in workloads]
        our_red   = [r["nsga2"].get(w, {}).get("reduction", 0) for w in workloads]
        x = np.arange(len(workloads))
        w = 0.35
        ax.bar(x - w/2, paper_red, w, label="Paper",          color="steelblue",  alpha=0.8)
        ax.bar(x + w/2, our_red,   w, label="Ours (improved)", color="darkorange", alpha=0.8)
        ax.set_xticks(x); ax.set_xticklabels([wl.split("-")[0] for wl in workloads], fontsize=9)
        ax.set_ylabel("Peak Aging Reduction (%)"); ax.set_title("NSGA-II: Peak Aging Reduction")
        ax.legend(); ax.grid(axis="y", alpha=0.3)

        # Plot 3: Predictor metrics comparison
        ax = axes[2]
        cats   = ["Pred R²", "Pred MAE×10", "Traj R²", "Traj MAE×10"]
        paper_v = [PAPER["predictor_r2"], PAPER["predictor_mae"]*10,
                   PAPER["trajectory_r2"], PAPER["trajectory_mae"]*10]
        our_v   = [r["predictor"]["r2"],   r["predictor"]["mae"]*10,
                   r["trajectory"]["r2"],  r["trajectory"]["mae"]*10]
        x = np.arange(len(cats)); w = 0.35
        ax.bar(x - w/2, paper_v, w, label="Paper",           color="steelblue",  alpha=0.8)
        ax.bar(x + w/2, our_v,   w, label="Ours (improved)", color="darkorange", alpha=0.8)
        ax.set_xticks(x); ax.set_xticklabels(cats, fontsize=9)
        ax.set_ylabel("Value"); ax.set_title("Model Accuracy (R² higher=better, MAE×10 lower=better)")
        ax.legend(); ax.grid(axis="y", alpha=0.3)

        plt.tight_layout()
        out = REPO_ROOT / "paper_comparison.png"
        plt.savefig(out, dpi=150, bbox_inches="tight")
        print(f"\n  Figures saved to {out}")
        plt.close()
    except ImportError:
        print("\n  [INFO] matplotlib not available — skipping figure generation")


def main():
    plot_mode = "--plot" in sys.argv
    results   = load_results()
    print_header()
    section_predictor(results)
    section_trajectory(results)
    section_nsga2(results)
    section_ppo(results)
    section_ablation()
    section_summary(results)
    if plot_mode:
        save_plots(results)


if __name__ == "__main__":
    main()

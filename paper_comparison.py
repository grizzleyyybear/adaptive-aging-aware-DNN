SEP  = "=" * 72
SEP2 = "-" * 72

def _delta(current, paper, higher_better=True):
    d = current - paper
    pct = d / max(abs(paper), 1e-9) * 100
    if higher_better:
        ok = current >= paper * 0.97
    else:
        ok = current <= paper * 1.10
    sym = "✓" if ok else "✗"
    sign = "+" if d >= 0 else ""
    return f"{sym}  Δ={sign}{d:+.4f} ({sign}{pct:+.1f}%)"


def load_results() -> dict:
    path = REPO_ROOT / "eval_results.json"
    if not path.exists():
        print(f"[ERROR] eval_results.json not found. Run: python run_eval.py --full")
        sys.exit(1)
    with open(path) as f:
        return json.load(f)


